public class Array2 {
    public static void main(String[] args) {
        int[] arr = {5, 10, 15, 20, 25};

        System.out.println("Traversing Array:");
        for (int i = 0; i < arr.length; i++) {
            System.out.println("Element at index " + i + " = " + arr[i]);
        }
    }
}